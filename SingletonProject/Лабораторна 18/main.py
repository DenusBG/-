# Тема: Обробка виключень у Python: підходи та застосування
# Мета: Навчитися будувати надійні програми за допомогою try/except

# --- ЗАВДАННЯ 1: Вступ та теорія ---
# try - блок коду, який ми тестуємо на помилки
# except - блок, що виконується при виникненні помилки
# else - виконується, якщо помилок НЕ було
# finally - виконується ЗАВЖДИ (наприклад, для закриття файлів)

print("--- ЗАВДАННЯ 2 & 3: Ділення та перевірка вводу ---")
try:
    # Обробимо ділення на нуль та помилку введення (Завдання 1-2 з прикладів)
    число1 = int(input("Введіть перше число: "))
    число2 = int(input("Введіть друге число: "))
    результат = число1 / число2
except ZeroDivisionError:
    print("Помилка: Не можна ділити на нуль! (ZeroDivisionError)")
except ValueError:
    print("Помилка: Ви ввели не число! (ValueError)")
else:
    print(f"Успіх! Результат: {результат}")
finally:
    print("Операція завершена.\n")


print("--- ЗАВДАННЯ 5: Власні виключення ---")
# Створюємо власний клас помилки
class MyCustomError(Exception):
    """Це моє власне виключення для перевірки віку"""
    pass

try:
    вік = int(input("Введіть ваш вік: "))
    if вік < 0 or вік > 120:
        raise MyCustomError("Вік не може бути меншим за 0 або більшим за 120!")
except MyCustomError as e:
    print(f"Спрацювало власне виключення: {e}")
except ValueError:
    print("Помилка: введіть число.")


print("\n--- ЗАВДАННЯ 6: Робота з файлами ---")
try:
    ім_я_файлу = input("Введіть назву файлу для читання (напр. test.txt): ")
    з_файл = open(ім_я_файлу, "r", encoding="utf-8")
    print(з_файл.read())
except FileNotFoundError:
    print(f"Помилка: Файл '{ім_я_файлу}' не знайдено! (FileNotFoundError)")
finally:
    print("Блок finally: Ресурси перевірено.")