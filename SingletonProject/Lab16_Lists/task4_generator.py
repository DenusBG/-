print("=== Виконання 40 операцій зі списками ===")

data = list(range(1, 42)) # Список чисел від 1 до 41

# Цикл виконає 40 ітерацій, роблячи різні дії
for i in range(40):
    val = data[i]
    
    if i < 10:
        # Завдання 1-10: Математика
        res = val * 2
        action = "Множення на 2"
    elif i < 20:
        # Завдання 11-20: Перевірка
        res = "Парне" if val % 2 == 0 else "Непарне"
        action = "Перевірка парності"
    elif i < 30:
        # Завдання 21-30: Форматування
        res = f"Item-{val}"
        action = "Створення рядка"
    else:
        # Завдання 31-40: Кортежі
        res = (val, val**2)
        action = "Створення кортежу"

    print(f"Завдання {i+1}: {action} для {val} -> Результат: {res}")