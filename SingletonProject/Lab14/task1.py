# Завдання 1: Читання текстового файлу

# Використовуємо конструкцію 'with', щоб файл закрився автоматично
# 'r' означає read (читання)
# encoding='utf-8' потрібен для коректного відображення кирилиці

try:
    with open('example.txt', 'r', encoding='utf-8') as file:
        print("--- Початок файлу ---")
        
        # Читаємо файл рядок за рядком
        for line in file:
            # end='' прибирає зайві переноси рядків, бо print сам додає один,
            # і в файлі в кінці рядка теж є невидимий перенос
            print(line, end='')
            
    print("\n--- Кінець файлу ---")

except FileNotFoundError:
    print("Помилка: Файл 'example.txt' не знайдено. Перевірте назву та розташування.")